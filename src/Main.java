public class Main {

    public static void main(String[] args) {
        // Declaring and initializing a variable of data type "int" that hold a whole number.
        int myFirstNumber = 5;

        // Declaring an "int" variable.
        int mySecondNumber;

        // Initializing "mySecondNumber" via assignment operator "=".
        mySecondNumber = 10;

        // Prints String literal "myFirstNumber".
        System.out.println("myFirstNumber");

        // Prints the value stored in "myFirstNumber".
        System.out.println(myFirstNumber);

        // Prints the value stored in "mySecondNumber".
        System.out.println(mySecondNumber);

        // Assigning a complex expression to "myFirstNumber".
        myFirstNumber = 10 + 5;

        // Prints the value stored in "myFirstNumber".
        System.out.println(myFirstNumber);

        // Assigning a complex expression to "myFirstNumber".
        myFirstNumber = (10 + 5) + (2 * 10);

        // Prints the value stored in "myFirstNumber".
        System.out.println(myFirstNumber);
    }
}
